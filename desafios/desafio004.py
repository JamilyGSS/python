res = input('Digite algo: ')
print('O tipo primitivo desse valor é: \033[4;97m{}\033[m'.format(type(res)))
print('Só tem espaços? \033[4;31m {} \033[m'.format(res.isspace()))
print('É um número?\033[4;32m  {} \033[m'.format(res.isnumeric()))
print('É alfabético? \033[4;33m {} \033[m'.format(res.isalpha()))
print('É alfanumérico? \033[4;34m {} \033[m'.format(res.isalnum()))
print('Está em maiúsculas? \033[4;35m {} \033[m'.format(res.isupper()))
print('Está em minúsculas? \033[4;36m {} \033[m'.format(res.islower()))
print('Está capitalizada? \033[4;37m {} \033[m'.format(res.istitle()))